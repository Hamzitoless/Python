'''
There are 2 types of functions
1.Performs a task - Displays output without being called(Not reusable)
2.Returns a value - Requires calling to display an output(Reusable)
'''
def increment(number,by):
    return number+by

#print(increment(4,2))
print(increment(4,by=2))
#The by is a keyword argument for better understanding

